public class WorldState {

	WorldState() {}

	@Override
	public int hashCode() { // Need to have equal objects produce the same hash code.
		final int prime = 31;
		int result = 1;
		result = prime * result;
		result = prime * result;
		return result;
	}

	public String toString() {
		return null + "." + null;
	}
}
